#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	size_t	*t;

	t = (size_t *)s;
	i = 0;
	while ((i < n) && (t[i]))
	{
		t[i] = 0;
		i++;
	}
}
